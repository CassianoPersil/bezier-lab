const LanguageContext = createContext<LanguageContextType | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('pt')

  useEffect(() => {
    const stored = localStorage.getItem('bezier-lang') as Language
    if (stored === 'pt' || stored === 'en') {
      setLanguageState(stored)
      document.documentElement.lang = stored === 'pt' ? 'pt-BR' : 'en'
    } else {
      const locale = navigator.language.slice(0, 2)
      const initial: Language = locale === 'pt' ? 'pt' : 'en'
      setLanguageState(initial)
      document.documentElement.lang = initial === 'pt' ? 'pt-BR' : 'en'
    }
  }, [])

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    localStorage.setItem('bezier-lang', lang)
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en'
  }

  const t = (key: string) => {
    return translations[language]?.[key] || translations['pt']?.[key] || key
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}
